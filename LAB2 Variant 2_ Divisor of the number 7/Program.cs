/* Вариант 2: 1. Напишите программы:
Проверить, является ли введенное число делителем числа 7. Вводятся два целых числа. Проверить делится ли первое на второе. Вывести на экран сообщение об этом, а также остаток (если он есть) и частное(в любом случае).
*/

class Program
{
    static void Main()
    {
        // Выводим сообщение, чтобы пользователь ввел первое число
        Console.Write("Введите первое число: ");

        // Считываем введенное пользователем первое число и преобразуем его в целое число (int)
        int number1 = Convert.ToInt32(Console.ReadLine());

        // Выводим сообщение, чтобы пользователь ввел второе число
        Console.Write("Введите второе число: ");

        // Считываем введенное пользователем второе число и преобразуем его в целое число (int)
        int number2 = Convert.ToInt32(Console.ReadLine());

        // Проверяем, является ли второе число делителем числа 7
        bool isDivisorOf7 = (7 % number2 == 0);

        // Выводим результат проверки
        if (isDivisorOf7)
        {
            Console.WriteLine($"{number2} является делителем числа 7.");
        }
        else
        {
            Console.WriteLine($"{number2} не является делителем числа 7.");
        }

        // Вычисляем частное от деления первого числа на второе
        int quotient = number1 / number2;

        // Вычисляем остаток от деления первого числа на второе
        int remainder = number1 % number2;

        // Выводим частное и остаток
        Console.WriteLine($"Частное: {quotient}");
        Console.WriteLine($"Остаток: {remainder}");

        // Ожидаем, пока пользователь нажмет Enter, чтобы закрыть консоль
        Console.WriteLine("Нажмите Enter для выхода...");
        Console.ReadLine();
    }
}
