// 2. Определите, является ли заданное число двузначным числом, оканчивающимся на 3.

class Program
{
    static void Main()
    {
        // Объявляем переменную для хранения введенного числа
        int number;

        // Выводим сообщение, чтобы пользователь ввел число
        Console.Write("Введите целое число: ");

        // Считываем введенное пользователем значение и преобразуем его в целое число (int)
        number = Convert.ToInt32(Console.ReadLine());

        // Проверяем, является ли число двузначным и оканчивается на 3
        if (number >= 10 && number <= 99 && number % 10 == 3)
        {
            Console.WriteLine("Введенное число является двузначным и оканчивается на 3.");
        }
        else
        {
            Console.WriteLine("Введенное число не соответствует условию.");
        }

        // Ожидаем, пока пользователь нажмет Enter, чтобы закрыть консоль
        Console.WriteLine("Нажмите Enter для выхода...");
        Console.ReadLine();
    }
}
